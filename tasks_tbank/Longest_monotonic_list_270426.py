import sys  # Для доступа к стандартным потокам (здесь - stdin)

def max_monotonic(nums: list[int]) -> tuple[int, int]: # Функция возвращает пару 
    # индексов (start, end) самого длинного монотонного подотрезка
    # Если массив пустой — возвращаем (-1, -1), дабы не прерывать поток/не падать с ошибкой
    if not nums:
        return -1, -1

    # best_start, best_end — границы лучшего (самого длинного) отрезка на данный момент
    best_start = 0
    best_end = 0

    # start_inc — старт текущего ВОЗРАСТАЮЩЕГО кусочка
    # start_dec — старт текущего УБЫВАЮЩЕГО кусочка
    start_inc = 0
    start_dec = 0

    # Идём по массиву начиная со второго элемента
    for i in range(1, len(nums)):
        # Смотрим пару (nums[i-1], nums[i])

        if nums[i] > nums[i - 1]:
            # Текущий элемент БОЛЬШЕ предыдущего:
            # значит, начатый ранее убывающий кусок "сломался" в точке i,
            # поэтому убывание сдвигаем/начинаем уже с позиции i,
            # а возрастающий отрезок продолжается (start_inc не трогаем)
            start_dec = i

        elif nums[i] < nums[i - 1]:
            # Текущий элемент МЕНЬШЕ предыдущего:
            # аналогично, возрастающий кусок "сломался" и сдвигается/начинается уже с i
            # а убывающий отрезок продолжается (start_dec не трогаем)
            start_inc = i

        else:
            # nums[i] == nums[i-1]:
            # равенство ломает И возрастающий, и убывающий отрезки,
            # поэтому оба сдвигаются/начинаются с позиции i
            start_inc = i
            start_dec = i

        # Теперь проверяем, не стал ли текущий возрастающий кусок (от start_inc до i)
        # длиннее, чем лучший найденный ранее
        if i - start_inc > best_end - best_start:
            best_start = start_inc
            best_end = i

        # И отдельно проверяем убывающий кусок (от start_dec до i)
        if i - start_dec > best_end - best_start:
            best_start = start_dec
            best_end = i
    # Возвращаем границы лучшего отрезка (не важно, возрастающий он или убывающий)
    return best_start, best_end 


def main():
    # Читаем всё из stdin, разбиваем по пробелам/переводам строки
    data = sys.stdin.read().strip().split()

    # Формат:
    # первая строка  — N
    # вторая строка — N чисел
    n = int(data[0])
    nums = list(map(int, data[1:1 + n]))

    # s — start, e — end найденного максимального монотонного подотрезка
    s, e = max_monotonic(nums)    
    # Выводим индексы через пробел
    print(s, e)


if __name__ == "__main__":    
    # Этот блок выполнится только если файл запущен как программа,
    # а при импорте модуля в другой файл max_monotonic просто будет 
    # доступна как функция
    main()